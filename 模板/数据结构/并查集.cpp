struct DSU {
    vector<int> f, siz;

    DSU(int n) {
        init(n);
    }

    void init(int n) {
        f.resize(n);
        iota(f.begin(), f.end(), 0);
        siz.assign(n, 1);
    }

    int find(int x) {
        while (x != f[x]) {
            x = f[x] = f[f[x]];
        }
        return x;
    }

    bool same(int x, int y) {
        return find(x) == find(y);
    }

    bool merge(int x, int y) {
        x = find(x), y = find(y);
        if (x == y) {
            return false;
        }
        siz[x] += siz[y];
        f[y] = x;
        return true;
    }

    int size(int x) {
        return siz[find(x)];
    }
};

void issue() {
    int n, m;
    cin >> n >> m;
    // 调用，构造一个大小是n的数组
    DSU ui(n);
    while (m--) {
        int a, x, y;
        cin >> a >> x >> y;
        if (a == 2) {
            if (ui.same(x, y))
                cout << "Y" << endl;
            else
                cout << "N" << endl;
        } else
            ui.merge(x, y);
    }
}

